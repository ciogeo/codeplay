<?php

/**
 * Class Calculator
 */
class Calculator {

    /**
     * @param $a
     * @param $b
     * @return mixed
     */
    public function add($a, $b)
    {
        return $a + $b;
    }

    /**
     * @param $a
     * @param $b
     * @return mixed
     */
    public function subtract($a, $b)
    {
        return $a - $b;
    }
}